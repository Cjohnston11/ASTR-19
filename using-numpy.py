print(f"np.arcsin({x}) = {np.arcsin(x)}")
print(f"np.arccos({x}) = {np.arccos(x)}")
print(f"np.arctan({x}) = {np.arctan(x)}")
print(f"np.arctan2({x}{y}) = {np.arctan2(x,y)}") #arctan (x/y)
print(f"np.rad2deg({x}) = {np.rad2deg(x)}") #convert rad to deg


